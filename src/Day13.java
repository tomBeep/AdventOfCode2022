    public static void main(String[] args) {
        new Day13().doChallenge();
    }

    private void doChallenge() {
        part1();
        part2();
    }

    private void part1() {
        String input = getInput();
        Scanner sc = new Scanner(input);
        sc.useDelimiter("");

        int index = 1;
        int sumOfValidIndexes = 0;
        while (sc.hasNext()) {
            if (parsePair(sc)) {
                sumOfValidIndexes += index;
            }
            index++;
        }
        System.out.println("Part 1: " + sumOfValidIndexes);
    }

    void part2() {
        String input = getInput();
        Scanner sc = new Scanner(input);
        sc.useDelimiter("");

        List<List<Item>> allLines = new ArrayList<>();
        while (sc.hasNextLine()) {
            if (sc.hasNext("\n")) {
                sc.next("\\n");
            } else {
                allLines.add(parseLine(sc));
            }
        }

        List<Item> extraLine1 = List.of(new Item(List.of(new Item(null, 2)), null));
        List<Item> extraLine2 = List.of(new Item(List.of(new Item(null, 6)), null));
        allLines.add(extraLine1);
        allLines.add(extraLine2);
        allLines.sort((a, b) -> compareLines(a, b) ? -1 : 1);
        int a = allLines.indexOf(extraLine1) + 1;
        int b = allLines.indexOf(extraLine2) + 1;
        System.out.println("Part 2: " + (a * b));
    }

    boolean parsePair(Scanner sc) {
        List<Item> left = parseLine(sc);
        List<Item> right = parseLine(sc);
        if (sc.hasNextLine()) {
            sc.next("\\n");
        }

        return compareLines(left, right);
    }

    Boolean compareLines(List<Item> left, List<Item> right) {
        // Returns null to indicate that we aren't sure whether the line is in order, the puzzle makes sure that all 
        // lines come to a conclusion though, so only the recursive calls will ever return null.
        for (int i = 0; i < left.size(); i++) {
            if (i >= right.size()) {
                return false;
            }
            Item leftItem = left.get(i);
            Item rightItem = right.get(i);
            if (leftItem.integer != null && rightItem.integer != null) {
                if (leftItem.integer < rightItem.integer) {
                    return true;
                }
                if (leftItem.integer > rightItem.integer) {
                    return false;
                }
            } else if (leftItem.list != null && rightItem.list != null) {
                Boolean b = compareLines(leftItem.list, rightItem.list);
                if (b != null) {
                    return b;
                }
            } else {
                // must be 1 list, 1 integer
                Boolean b;
                if (leftItem.list != null) {
                    b = compareLines(leftItem.list, List.of(new Item(null, rightItem.integer)));
                } else {
                    b = compareLines(List.of(new Item(null, leftItem.integer)), rightItem.list);
                }
                if (b != null) {
                    return b;
                }
            }
        }
        if (left.size() < right.size()) {
            return true;
        }
        return null;

    }

    List<Item> parseLine(Scanner sc) {
        List<Item> items = parseList(sc);
        sc.next("\\n");
        return items;
    }

    List<Item> parseList(Scanner sc) {
        List<Item> itemsInList = new ArrayList<>();
        sc.next("\\[");
        while (!sc.hasNext("]")) {
            if (isList(sc)) {
                List<Item> listItem = parseList(sc);
                itemsInList.add(new Item(listItem, null));
            } else {
                int intItem = parseInt(sc);
                itemsInList.add(new Item(null, intItem));
            }
            parseOptionalComma(sc);
        }
        sc.next("]");
        return itemsInList;
    }

    void parseOptionalComma(Scanner sc) {
        if (sc.hasNext(",")) {
            sc.next(",");
        }
    }

    boolean isList(Scanner sc) {
        return sc.hasNext("\\[");
    }

    int parseInt(Scanner sc) {
        StringBuilder number = new StringBuilder();
        // We're parsing 1 character at a time, so have to match multiple digits to work with 2+ digit numbers.
        while (sc.hasNext("\\d")) {
            number.append(sc.next("\\d"));
        }
        return Integer.parseInt(number.toString());
    }

    private class Item {
        private List<Item> list;
        private Integer integer;

        public Item(List<Item> list, Integer integer) {
            // A somewhat sad way to do a union of two types.
            if (list != null && integer != null) {
                throw new RuntimeException("Either list or integer must be null");
            }
            this.list = list;
            this.integer = integer;
        }
    }

